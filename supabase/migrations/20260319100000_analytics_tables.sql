-- 1. message_events - trilha granular de eventos de entrega
CREATE TABLE IF NOT EXISTS public.message_events (
  id UUID PRIMARY KEY DEFAULT gen_random_uuid(),
  message_id UUID REFERENCES public.patients_queue(id) ON DELETE CASCADE,
  instance_id UUID REFERENCES public.whatsapp_instances(id) ON DELETE SET NULL,
  to_phone TEXT,
  event_type TEXT NOT NULL CHECK (
    event_type IN (
      'send_accepted',
      'delivered',
      'read',
      'replied',
      'failed',
      'not_received_timeout',
      'followup_sent',
      'retry_phone2_sent',
      'status_queued',
      'status_sending',
      'status_delivered',
      'status_failed',
      'status_cancelled'
    )
  ),
  event_at TIMESTAMPTZ NOT NULL DEFAULT NOW(),
  raw_payload JSONB,
  created_at TIMESTAMPTZ NOT NULL DEFAULT NOW()
);

COMMENT ON TABLE public.message_events IS 'Eventos granulares de mensageria para analytics e trilha de status';
COMMENT ON COLUMN public.message_events.raw_payload IS 'Payload resumido do evento para auditoria e depuracao';

CREATE INDEX IF NOT EXISTS idx_message_events_message_id
  ON public.message_events(message_id);

CREATE INDEX IF NOT EXISTS idx_message_events_event_type
  ON public.message_events(event_type, event_at DESC);

CREATE INDEX IF NOT EXISTS idx_message_events_instance_id
  ON public.message_events(instance_id);

CREATE INDEX IF NOT EXISTS idx_message_events_daily
  ON public.message_events(((event_at AT TIME ZONE 'America/Cuiaba')::date), event_type);

CREATE INDEX IF NOT EXISTS idx_message_events_message_id_event_type
  ON public.message_events(message_id, event_type);

ALTER TABLE public.message_events ENABLE ROW LEVEL SECURITY;

DO $$
BEGIN
  IF NOT EXISTS (
    SELECT 1 FROM pg_policies
    WHERE schemaname = 'public'
      AND tablename = 'message_events'
      AND policyname = 'Allow authenticated operations on message_events'
  ) THEN
    CREATE POLICY "Allow authenticated operations on message_events"
      ON public.message_events FOR ALL TO authenticated USING (true) WITH CHECK (true);
  END IF;
END $$;

-- 2. analytics_daily - agregado diario para dashboard principal
CREATE TABLE IF NOT EXISTS public.analytics_daily (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  data DATE NOT NULL UNIQUE,
  total_enviadas INTEGER NOT NULL DEFAULT 0,
  sucesso INTEGER NOT NULL DEFAULT 0,
  falha INTEGER NOT NULL DEFAULT 0,
  cancelada INTEGER NOT NULL DEFAULT 0,
  por_procedimento JSONB NOT NULL DEFAULT '{}'::jsonb,
  avg_time_to_delivery_minutes NUMERIC(10,2),
  avg_retry_count NUMERIC(10,2),
  instances_used INTEGER NOT NULL DEFAULT 0,
  created_at TIMESTAMPTZ NOT NULL DEFAULT NOW(),
  updated_at TIMESTAMPTZ NOT NULL DEFAULT NOW()
);

COMMENT ON TABLE public.analytics_daily IS 'Agregado diario para dashboard principal e historico de performance';
COMMENT ON COLUMN public.analytics_daily.por_procedimento IS 'Objeto JSON com contadores por procedimento para o dia';

CREATE INDEX IF NOT EXISTS idx_analytics_daily_data_desc
  ON public.analytics_daily(data DESC);

