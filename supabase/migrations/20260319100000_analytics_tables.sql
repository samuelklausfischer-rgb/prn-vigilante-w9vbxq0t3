  data_exame TEXT,
  Data_nascimento TEXT,
  procedimentos TEXT,
  time_proce TEXT,
  horario_inicio TIME,
  horario_final TIME,
  phone_2 TEXT,
  phone_3 TEXT,
  is_landline BOOLEAN,
  locked_by TEXT,
  locked_at TIMESTAMPTZ,
